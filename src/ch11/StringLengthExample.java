package ch11;

public class StringLengthExample {

	public static void main(String[] args) {
		
		String ssn = "7306241230123";
		int length = ssn.length(); //ssn의 문자 길이수를 구하기

		if(length ==13) {
			System.out.println("주민번호 자리수가 맞습니다.");
		} else {
			System.out.println("주민번호 자리수가 맞습니다.");
		}
	
	}

}
